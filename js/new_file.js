//定义数字
var index = 0;
//定义一个。。。flag
var flag = true;

//获取高度
box.style.height = li.length * liHeight + "px";

//不知道为什么最后一张图加上内容按钮也有bug了
butR.onclick = function() {
    //判断flag是否为true
    if (flag) {
        //更改flag为false
        flag = false;

        index++;
        //判断数字是否等于图片的总数量
        if (index == li.length) {

            index = 0;
        };
        //向下移动
        box.style.top = -index * liHeight + "px";
        //查找小圆点下标位置
        for (var i = 0; i < dot.length; i++) {
            //判断小圆点的下标位置是否与数字相同
            if (i == index) {
                dot[index].id = "dotIs";
            } else {

                dot[i].id = "";
            };
        };
        //定时器
        setTimeout(function() {
            //改变flag为true
            flag = true;
        }, 500)
    };
};

